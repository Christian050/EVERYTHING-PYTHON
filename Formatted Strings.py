first = 'Christian'
last = 'Smith'

# Concatination
message = first + ' [' + last + '] is a coder'
print(message)


# Using formated string(s)
msg = f'{first} [{last}] is a coder'
print(msg)


# The '{}' acts as a (defines) placeholder or 'holes' in the string.
# It dynamically inserts values into the string.